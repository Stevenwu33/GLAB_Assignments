package PA303_2_3_OperatorsAndNumbers;

public class Question6 {

    public static void main(String[] args) {

        int x = 1;
        x++;
        System.out.println("First implementation to increment by 1:\n x++;  x =  "  + x);

        int y = 1;
        y += 1;
        System.out.println("Second implementation to increment by 1:\n y += 1; y =  "  + y);

        int z = 1;
        z = z + 1;
        System.out.println("Third implementation to increment by 1:\n z = z + 1; z =  " + z);


    }
}
