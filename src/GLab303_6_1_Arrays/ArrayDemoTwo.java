package GLab303_6_1_Arrays;

public class ArrayDemoTwo {
    public static void main(String[] args) {

        int[] age = {12, 4, 5};

        System.out.println("Using the For loop to iterate over the Array: ");

        for(int i = 0; i<age.length;  i++){
            System.out.println(age[i]);
        }
    }
}
